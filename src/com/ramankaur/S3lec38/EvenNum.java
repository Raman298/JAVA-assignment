package com.ramankaur.S3lec38;

public class EvenNum {

        public static void main(String args[]) {

            int a = 4;
            if (a % 2 == 0) {
                System.out.println(a + " is even number");
            } else {
                System.out.println(a + " is odd number");
            }
        }

}
